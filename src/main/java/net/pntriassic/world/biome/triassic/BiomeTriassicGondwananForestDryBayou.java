		{
			if (rand.nextInt(8) != 0) {return NULL_TREE;}

			if (Math.random() > 0.1) {
				return DICROIDIUM_H_TREE;
			}
			else {
				if (Math.random() > 0.5) {
					return GINKGO_TREE;
				}
				else {
					if (Math.random() > 0.5) {
						return SPHENOBAIERA;
					}
					else {
						return ARAUCARIOXYLON_TREE;
					}
				}
			}
		}

